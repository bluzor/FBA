            <h1>KEJU</h1>
            <h3>Deskripsi</h3>
            <p>Keju adalah produk olahan susu yang dihasilkan melalui koagulasi protein kasein. Biasanya, keju dibuat dengan menggunakan susu sapi, kambing, atau domba. Proses pembuatan keju melibatkan penggunaan bakteri atau enzim untuk mengentalkan susu, yang kemudian dipotong dan dikeringkan untuk menghasilkan keju dengan berbagai tekstur dan rasa.</p>
            <h3>Cara Pembuatan Keju</h3>
            <ol>
                <li><strong>Persiapkan Bahan:</strong> Anda memerlukan susu segar, kultur starter (bakteri baik), rennet (enzim pembekuan), dan garam.</li>
                <li><strong>Pasteurisasi Susu:</strong> Panaskan susu hingga suhu sekitar 72°C untuk membunuh bakteri yang tidak diinginkan, lalu dinginkan hingga suhu 30°C.</li>
                <li><strong>Tambahkan Kultur Starter:</strong> Masukkan kultur starter ke dalam susu yang telah didinginkan dan biarkan selama 30-60 menit.</li>
                <li><strong>Tambahkan Rennet:</strong> Tambahkan rennet ke dalam susu dan aduk perlahan. Biarkan campuran ini selama 30-60 menit hingga mengental.</li>
                <li><strong>Pemotongan Curd:</strong> Setelah mengental, potong curd (bagian padat) menjadi kubus-kubus kecil untuk mengeluarkan whey (bagian cair).</li>
                <li><strong>Masak dan Aduk Curd:</strong> Panaskan curd secara perlahan sambil terus diaduk hingga mencapai suhu 40°C. Ini membantu mengeluarkan lebih banyak whey.</li>
                <li><strong>Pengeringan Curd:</strong> Tiriskan curd dan biarkan mengering. Tambahkan garam sesuai selera.</li>
                <li><strong>Pencetakan dan Pematangan:</strong> Masukkan curd ke dalam cetakan keju dan tekan untuk membentuk. Biarkan keju matang pada suhu tertentu sesuai jenis keju yang diinginkan.</li>
            </ol>
            <p>Keju siap dinikmati setelah melalui proses pematangan yang bervariasi, mulai dari beberapa minggu hingga beberapa bulan, tergantung pada jenis keju.</p>
