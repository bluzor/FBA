            <h3>Deskripsi</h3>
            <p>Yogurt adalah produk olahan susu yang dihasilkan melalui fermentasi bakteri. Bakteri yang digunakan untuk membuat yogurt disebut kultur yogurt, yang memfermentasi laktosa, gula alami dalam susu. Proses ini menghasilkan asam laktat, yang memberikan yogurt tekstur dan rasa khasnya.</p>
            <h3>Cara Pembuatan Yogurt</h3>
            <ul>
                <li><strong>Siapkan Bahan:</strong> Anda membutuhkan susu segar dan starter yogurt (yogurt dengan kultur hidup).</li>
                <li><strong>Panaskan Susu:</strong> Panaskan susu hingga suhu sekitar 85°C untuk membunuh bakteri yang tidak diinginkan dan untuk memodifikasi protein susu agar menghasilkan tekstur yogurt yang lebih kental.</li>
                <li><strong>Dinginkan Susu:</strong> Biarkan susu mendingin hingga suhu sekitar 43°C.</li>
                <li><strong>Tambahkan Starter Yogurt:</strong> Campurkan starter yogurt dengan susu yang telah didinginkan. Aduk hingga merata.</li>
                <li><strong>Fermentasi:</strong> Tuangkan campuran susu dan starter ke dalam wadah yang bersih dan hangatkan pada suhu sekitar 43°C selama 6-12 jam. Waktu fermentasi dapat disesuaikan tergantung pada keinginan Anda akan kekentalan dan rasa yogurt.</li>
                <li><strong>Penyimpanan:</strong> Setelah fermentasi selesai, simpan yogurt di dalam lemari es untuk menghentikan proses fermentasi. Yogurt siap dinikmati.</li>
            </ul>
